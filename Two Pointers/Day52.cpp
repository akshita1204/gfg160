class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        // Your code here
        int c=0;
        sort(arr.begin(),arr.end());
        // 1 2 2 3 4 5 
        int i=0,j=arr.size()-1;
        while(i<j)
        {
            if(arr[i]+arr[j]<target) 
            {
                c+=(j-i);
                i++;
            }
            else if(arr[i]+arr[j]>=target) j--;
        }
        return c;
    }
};


