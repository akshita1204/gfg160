class Solution {
  public:
    vector<int> sumClosest(vector<int>& arr, int target) {
        // code here
        int curdif=INT_MAX; 
        vector<int>ans;
        sort(arr.begin(),arr.end());
        int i=0,j=arr.size()-1;
        while(i<j)
        {
            int sum=arr[i]+arr[j];
            if(abs(target-sum)<curdif)
            {
                curdif=abs(target-sum);
                ans.clear();
                ans.push_back(arr[i]);
                 ans.push_back(arr[j]);
            }
            if(sum<target) i++;
            else j--;
        }
        return ans;
    }
};

