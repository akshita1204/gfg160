class Solution {
  public:
    // Function to rotate a linked list.
    Node* rotate(Node* head, int k) {
        // Your code here
             if(head==NULL || head->next==NULL) return head;
        int l=0;
        Node* temp=head;
        Node* tail=NULL;
        while(temp!=NULL)
        {
            if(temp->next==NULL) tail=temp;
            temp=temp->next;
            l++;
        }
        k=k%l;
        if(k==0) return head;
        temp=head;
        for(int i=1;i<k;i++)
        {
            temp=temp->next;
        }
        tail->next=head;
        head=temp->next;
        temp->next=NULL;
        
        return head;
    }
};



